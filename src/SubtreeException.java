public class SubtreeException extends java.lang.Exception
{
  
  /**
   * Creates a new instance of <code>SubtreeException</code> without detail message.
   */
  public SubtreeException()
  {
  }
  
  
  /**
   * Constructs an instance of <code>SubtreeException</code> with the specified detail message.
   * @param msg the detail message.
   */
  public SubtreeException(String msg)
  {
    super(msg);
  }
}
